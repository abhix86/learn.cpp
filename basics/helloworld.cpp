#include <iostream> // Include the iostream library for input and output operations

int main() { 
    std::cout << "Hello, World!" << std::endl; 
    std::cout << "Howdy?" << '\n';
    std::cout << "I'm good, thanks for asking\n";
    /*
        So, in this program we are using the iostream library to print "Hello, World!" to the console.
        The std::cout is used to output data to the standard output stream (usually the console).
        The std::endl is used to insert a new line and flush the output buffer.
        The '\n' is another way to insert a new line.   
        The other way to insert a new line is to add `\n` to the end of the string.
    */
    return 0; 
}
